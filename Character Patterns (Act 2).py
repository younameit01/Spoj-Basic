t = int(input())
for k in range(t):
    l, c = input().split(' ')
    l = int(l)
    c = int(c)
    for i in range(int(l)):
        for j in range(int(c)):
            if (i == 0) or (i == l-1) or (j == 0) or (j == c-1):
                print('*', end = '')
            else:
                print('.', end = '')
        print('\n')
